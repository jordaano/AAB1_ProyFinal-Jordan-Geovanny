    private void btnRegresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegresarActionPerformed
     if (restauranteOrigen != null) {
        switch (restauranteOrigen) {
            case "Spanish":
                SpanishFood esp = new SpanishFood();
                esp.setVisible(true);
                break;
            case "Ecuadorian":
                EcuadorianFood ecu = new EcuadorianFood();
                ecu.setVisible(true);
                break;
            case "Chinese":
                ChineseFood chi = new ChineseFood();
                chi.setVisible(true);
                break;
            case "Italian":
                ItalianFood ita = new ItalianFood();
                ita.setVisible(true);
                break;
            default:
            
                Comidas menu = new Comidas();
                menu.setVisible(true);
                break;
        }
    } else {
        Comidas menu = new Comidas();
        menu.setVisible(true);
    }
    
    this.dispose(); 

    }//GEN-LAST:event_btnRegresarActionPerformed

    

